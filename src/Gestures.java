/**
 * Enum-class that defines all the possible internal names for gestures that can be detected
 */
public enum Gestures {
    MouseSwipeDown,
    MouseSwipeUp,
    MouseSwipeLeft,
    MouseSwipeRight,
    MouseCircle,
    MouseKeytap,
    KeyboardSwipeDown,
    KeyboardSwipeUp,
    KeyboardSwipeLeft,
    KeyboardSwipeRight,
    KeyboardCircle,
    KeyboardKeytap,
    NONE
}
